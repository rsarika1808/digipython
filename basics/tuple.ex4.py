#Accessing tuple element using slicing
my_tuple =('e','x','c','a','1','i','b','u','r')

#element 2nd to 4th
#output:('x','c','a')
print(my_tuple[1:4])

#element beginning to 2nd
#output;('e','x')
print(my_tuple[:-7])

#element 8th to end
#output:('u','r')
print(my_tuple[7:])

#elementbeginning to end
print(my_tuple[:])


